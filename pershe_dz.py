""" Перше завдання
Створіть функцію get_days_from_today(date), яка розраховує кількість днів між заданою датою і поточною датою.
Вимоги до завдання:
Функція приймає один параметр: date — рядок, що представляє дату у форматі 'РРРР-ММ-ДД' (наприклад, '2020-10-09').
Функція повертає ціле число, яке вказує на кількість днів від заданої дати до поточної. Якщо задана дата пізніша
 за поточну, результат має бути від'ємним.
У розрахунках необхідно враховувати лише дні, ігноруючи час (години, хвилини, секунди).
Для роботи з датами слід використовувати модуль datetime Python.
"""



from datetime import datetime
def get_days_from_today(date):
    try:
        given_date = datetime.strptime(date,"%Y-%m-%d")
        today = datetime.today()
        delta = today - given_date
        return delta.days
    except ValueError:
        return "Неправильний формат дати. Очікуеться 'РРРР-ММ-ДД'"
print(get_days_from_today("2021-05-05"))


"""
 Друге завдання
Щоб виграти головний приз лотереї, необхідний збіг кількох номерів на лотерейному квитку з числами, що випали 
випадковим чином і в певному діапазоні під час чергового тиражу. Наприклад, необхідно вгадати шість чисел від
 1 до 49 чи п'ять чисел від 1 до 36 тощо.
Вам необхідно написати функцію get_numbers_ticket(min, max, quantity), яка допоможе генерувати набір унікальних
 випадкових чисел для таких лотерей.
Вона буде повертати випадковий набір чисел у межах заданих параметрів, причому всі випадкові числа в наборі
 повинні бути унікальні.
Вимоги до завдання:
Параметри функції:
min - мінімальне можливе число у наборі (не менше 1).
max - максимальне можливе число у наборі (не більше 1000).
quantity - кількість чисел, які потрібно вибрати (значення між min і max).
Функція генерує вказану кількість унікальних чисел у заданому діапазоні.
Функція повертає список випадково вибраних, відсортованих чисел. Числа в наборі не повинні повторюватися.
 Якщо параметри не відповідають заданим обмеженням, функція повертає пустий список.
 """

import random
def get_numbers_ticket(min, max, quantiti):
    if min < 1 or max > 1000 or quantiti > (max - min + 1) or quantiti <= 0:
        return []
    numbers = random.sample(range(min, max + 1), quantiti)
    return sorted(numbers)
lottery_numbers = get_numbers_ticket(1, 49, 6)
print(f"Ваші лотерейні числа: {lottery_numbers}")


"""
Третє завдання (не обов'язкове)
У вашій компанії ведеться активна маркетингова кампанія за допомогою SMS-розсилок. Для цього ви збираєте телефонні
номери клієнтів із бази даних, але часто стикаєтеся з тим, що номери записані у різних форматах. Наприклад:
Ваш сервіс розсилок може ефективно відправляти повідомлення лише тоді, коли номери телефонів представлені у
коректному форматі. Тому вам необхідна функція, яка автоматично нормалізує номери телефонів до потрібного формату,
видаляючи всі зайві символи та додаючи міжнародний код країни, якщо потрібно.
Розробіть функцію normalize_phone(phone_number), що нормалізує телефонні номери до стандартного формату, залишаючи
тільки цифри та символ '+' на початку. Функція приймає один аргумент - рядок з телефонним номером у будь-якому
форматі та перетворює його на стандартний формат, залишаючи тільки цифри та символ '+'. Якщо номер не містить
міжнародного коду, функція автоматично додає код '+38' (для України). Це гарантує, що всі номери будуть
придатними для відправлення SMS.

Вимоги до завдання:

Параметр функції phone_number - це рядок з телефонним номером у різноманітних форматах.
Функція видаляє всі символи, крім цифр та символу '+'.
Якщо міжнародний код відсутній, функція додає код '+38'. Це враховує випадки, коли номер починається з '380'
(додається лише '+') та коли номер починається без коду (додається '+38').
Функція повертає нормалізований телефонний номер у вигляді рядка."""


import re
def normalize_phone(phone_number):
    sanitized_number = re.sub(r'[^\d+]', '', phone_number)
    if not sanitized_number.startswith('+'):
        if sanitized_number.startswith('380'):
            sanitized_number = '+' + sanitized_number
        else:
            sanitized_number = '+38' + sanitized_number
    return sanitized_number
raw_numbers = [
    "067\\t123 4567",
    "(095) 234-5678\\n",
    "+380 44 123 4567",
    "380501234567",
    "    +38(050)123-32-34",
    "     0503451234",
    "(050)8889900",
    "38050-111-22-22",
    "38050 111 22 11   ",
]

sanitized_numbers = [normalize_phone(num) for num in raw_numbers]
print("Нормалізовані номери телефонів для SMS-розсилки:", sanitized_numbers)
  
